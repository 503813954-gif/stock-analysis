  renderTrend(trend) {
    const section = document.getElementById('trendSection');
    if (!trend) { section.style.display = 'none'; return; }
    section.style.display = 'block';

    const periods = [
      { key: 'short', el: 'trendShort', name: '短期', scope: '1-2周' },
      { key: 'mid', el: 'trendMid', name: '中期', scope: '1-3月' },
      { key: 'long', el: 'trendLong', name: '长期', scope: '3月+' }
    ];

    periods.forEach(p => {
      const data = trend[p.key];
      const card = document.getElementById(p.el);
      if (!data) return;

      card.className = `trend-card ${data.cls}`;
      card.querySelector('.trend-score').textContent = data.score;
      card.querySelector('.trend-label').innerHTML = `${data.icon} ${data.label}`;
      card.querySelector('.trend-detail').textContent = data.detail;
    });

    // MA summary
    if (trend.maData) {
      const ma = trend.maData;
      let maHtml = '均线: ';
      const parts = [];
      if (ma.ma5) parts.push(`MA5 <span class="ma-value">${ma.ma5}</span>`);
      if (ma.ma10) parts.push(`MA10 <span class="ma-value">${ma.ma10}</span>`);
      if (ma.ma20) parts.push(`MA20 <span class="ma-value">${ma.ma20}</span>`);
      if (ma.ma60) parts.push(`MA60 <span class="ma-value">${ma.ma60}</span>`);
      maHtml += parts.join(' · ');

      // Check alignment
      const ma5f = parseFloat(ma.ma5), ma10f = parseFloat(ma.ma10);
      const ma20f = parseFloat(ma.ma20), ma60f = parseFloat(ma.ma60);
      if (ma5f && ma10f && ma20f) {
        maHtml += ' · 排列: ';
        if (ma5f > ma10f && ma10f > ma20f && (!ma60f || ma20f > ma60f)) {
          maHtml += '<span class="ma-bullish">多头排列 ↑</span>';
        } else if (ma5f < ma10f && ma10f < ma20f && (!ma60f || ma20f < ma60f)) {
          maHtml += '<span class="ma-bearish">空头排列 ↓</span>';
        } else {
          maHtml += '<span class="ma-neutral">交叉缠绕</span>';
        }
      }
      document.getElementById('maSummary').innerHTML = maHtml;
    }
  },

