  renderAccuracy(data) {
    if (!data || data.length < 60) return;
    const section = document.getElementById('accuracySection');
    section.style.display = 'block';
    const closes = data.map(d => d.close);

    let bullUp = 0, bullTotal = 0, bearDown = 0, bearTotal = 0;
    let shortRight = 0, shortTotal = 0;
    let midRight = 0, midTotal = 0;
    let buy6Up = 0, buy6Total = 0;
    let sell6Down = 0, sell6Total = 0;

    // Use every 5th day to avoid overlapping signals
    const LOOKAHEAD = 5;
    const MIN_MOVE = 0.01; // 1% minimum move to count

    for (let i = 60; i < data.length - LOOKAHEAD; i += 2) {
      const histData = data.slice(0, i + 1);
      const ind = calculateAllIndicators(histData);
      const dec = makeDecision(ind);
      const buyRatio = dec.buyRatio;
      const sellRatio = 1 - buyRatio;
      const currentPrice = closes[i];
      const futurePrice = closes[i + LOOKAHEAD];
      const pctChange = (futurePrice - currentPrice) / currentPrice;
      const wentUp = pctChange > MIN_MOVE;
      const wentDown = pctChange < -MIN_MOVE;

      // Short-term (KDJ + RSI)
      shortTotal++;
      const shortBull = ind.kdj.bullish || ind.rsi.bullish;
      if (shortBull && wentUp) shortRight++;
      else if (!shortBull && wentDown) shortRight++;

      // Mid-term (MACD + DMI + MA)
      midTotal++;
      const midBull = ind.macd.bullish || ind.dmi.bullish || ind.ma.bullish;
      if (midBull && wentUp) midRight++;
      else if (!midBull && wentDown) midRight++;

      // Strong signal (weighted >= 70%)
      if (buyRatio >= 0.70) {
        bullTotal++;
        if (wentUp) bullUp++;
      } else if (sellRatio >= 0.70) {
        bearTotal++;
        if (wentDown) bearDown++;
      }

      // Mild signal (weighted >= 55%)
      if (buyRatio >= 0.55) {
        buy6Total++;
        if (wentUp) buy6Up++;
      } else if (sellRatio >= 0.55) {
        sell6Total++;
        if (wentDown) sell6Down++;
      }
    }

    const calc = (n, d) => d > 0 ? (n / d * 100) : 0;
    const bullAcc = calc(bullUp, bullTotal);
    const bearAcc = calc(bearDown, bearTotal);
    const shortAcc = calc(shortRight, shortTotal);
    const midAcc = calc(midRight, midTotal);
    const overallCases = bullTotal + bearTotal;
    const overallRight = bullUp + bearDown;
    const overallAcc = calc(overallRight, overallCases);
    const buy6Acc = calc(buy6Up, buy6Total);
    const sell6Acc = calc(sell6Down, sell6Total);

    const accHtml = `
      <div class="accuracy-grid">
        <div class="accuracy-card">
          <div class="acc-header">综合信号(7/10)</div>
          <div class="acc-value ${overallAcc >= 50 ? 'up' : 'down'}">${overallAcc.toFixed(1)}%</div>
          <div class="acc-detail">${overallRight}/${overallCases} 次</div>
        </div>
        <div class="accuracy-card">
          <div class="acc-header">综合信号(6/10)</div>
          <div class="acc-value ${buy6Acc >= 50 ? 'up' : 'down'}">${buy6Acc.toFixed(1)}%</div>
          <div class="acc-detail">看多${buy6Total}次·涨${buy6Up}次</div>
        </div>
        <div class="accuracy-card">
          <div class="acc-header">看空信号(6/10)</div>
          <div class="acc-value ${sell6Acc >= 50 ? 'up' : 'down'}">${sell6Acc.toFixed(1)}%</div>
          <div class="acc-detail">看空${sell6Total}次·跌${sell6Down}次</div>
        </div>
        <div class="accuracy-card">
          <div class="acc-header">短期(KDJ+RSI)</div>
          <div class="acc-value ${shortAcc >= 50 ? 'up' : 'down'}">${shortAcc.toFixed(1)}%</div>
          <div class="acc-detail">${shortRight}/${shortTotal} 次</div>
        </div>
        <div class="accuracy-card">
          <div class="acc-header">中期(MACD+MA+DMI)</div>
          <div class="acc-value ${midAcc >= 50 ? 'up' : 'down'}">${midAcc.toFixed(1)}%</div>
          <div class="acc-detail">${midRight}/${midTotal} 次</div>
        </div>
        <div class="accuracy-card">
          <div class="acc-header">回溯区间</div>
          <div class="acc-value" style="font-size:14px">${data[60]?.date || '--'}</div>
          <div class="acc-detail">→ ${data[data.length-1]?.date || '--'} · 预测${LOOKAHEAD}天 · 需波动>${(MIN_MOVE*100).toFixed(0)}%</div>
        </div>
      </div>
    `;
    document.getElementById('accuracyContent').innerHTML = accHtml;
  },

